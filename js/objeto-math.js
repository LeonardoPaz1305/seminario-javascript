//OBJECT MATH

// Obtener el numero PI
console.log(Math.PI);

// Mostrar solo 2 decimales
console.log(Math.PI.toFixed(4));

// Quita los decimales
console.log(Math.floor(Math.PI));

// Redondear un numero
console.log(Math.round(4.5));
console.log(Math.round(Math.PI));

// Pasar un numero negativo a su valor absoluto o positivo
console.log(Math.abs(-404));

// Calcular la potencia
console.log(Math.pow(2, 3));
console.log(2 ** 3);

// Crear un numero aleatorio entre 0 y 1 (no incluye 1)
console.log(Math.random());

// Crear un numero aleatorio entre 0 y 10
console.log(Math.random() * 10 + 1);

// Crear u numero aleatorio entre 0 y 10 de un Decimal
console.log(Math.floor(Math.random() * 100 + 1));